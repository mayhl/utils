// Package shellinit generates the shell integration that `mu shell-init` prints
// for `eval "$(mu shell-init)"` at shell startup. It emits a shared per-node
// dispatcher plus one thin wrapper per configured node, all driven by the same
// config.toml the engine reads — so adding a node in the config makes its command
// appear, with no hand-maintained alias codegen.
package shellinit

import (
	"fmt"
	"os"
	"strings"

	"github.com/mayhl/mayhl_utils/internal/config"
)

// The dispatcher: bare `<node>` connects (interactive ssh), `<node> push|pull`
// transfer via the engine, and `<node> <anycmd>` runs that command over ssh. It
// leans on the shell framework's seam helpers (mu_auth, mu_ssh_login, $MU_SSH).
const helper = `_mu_node() {
  local node=$1 target=$2; shift 2
  case ${1:-} in
    push) shift; mu cp push "$node" "$@" ;;
    pull) shift; mu cp pull "$node" "$@" ;;
    "")   mu_auth && mu_ssh_login "$target" ;;
    *)    mu_auth && ${MU_SSH:-ssh} "$target" "$@" ;;
  esac
}
`

// Generate returns the shell code to eval at startup. The current system
// ($MU_NODE, else $BC_HOST) is skipped — no self ssh/cp dispatcher.
func Generate() string {
	targets := config.NodeTargets()
	self := selfNode()

	var b strings.Builder
	b.WriteString("# mayhl_utils shell integration — generated by `mu shell-init` (do not edit)\n")
	b.WriteString(helper)
	for _, n := range config.NodeNames() {
		if n == self {
			continue
		}
		fmt.Fprintf(&b, "%s() { _mu_node %s \"%s\" \"$@\"; }\n", n, n, targets[n])
	}
	return b.String()
}

// selfNode is the node this shell is running on, if any (compute/login nodes set
// $BC_HOST reliably; $MU_NODE is an explicit override).
func selfNode() string {
	if n := os.Getenv("MU_NODE"); n != "" {
		return n
	}
	return os.Getenv("BC_HOST")
}
